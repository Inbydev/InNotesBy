### Seres humanos

- Primero el ser humano existe y luego tiene su esencia

ya que, un ser humano en la filosofía del existencialismo, no lo pueden definir como algo o alguien, ya que ahí le estarían dando una esencia y eso sería quitarle su libertad y autenticidad.

- Todos los seres humanos nacen libres, ya que nadie los define ni los limita.


Somos responsables de nuestras acciones y sus consecuencias. Nuestras acciones pueden influir en los demás y cambiar las normas sociales. Por lo tanto, es importante ser conscientes de cómo nuestras elecciones afectan a los demás y al mundo.

Ejemplo: &quot;una persona no saluda&quot;, la buena acción es saludar, si esta persona no saluda otro puede no saludar también, entonces, &quot;no saludar&quot; se comienza a ver como una buena acción. Una acción puede replicarse y cambiar la norma.



##Los fundamentos de la moral##<br />
<br />
**La naturaleza humana;** Todos se deben comportar según corresponda a esto. Todas las normas se cumplen por igual.<br />
<br />
**El deber;** Con nuestras acciones, creamos normas morales y éticas. Cuando consideramos una norma importante y valiosa, sentimos el deber de cumplirla porque está en línea con nuestros valores personales. <br />
<br />
**El amor;** Amor al prójimo y a ti mismo. "no hagas al otro lo que no te gustaría que te hagan a ti". Es para que se respeten y para que exista una unión. Según la frase es como "respeta para que el Karma no te llegue".<br />
Cuando hay una persona sin amor propio está _**ahí**_ para romper las normas porque no _**me**_ importa lo que me pase.<br />
<br />
**El contrato social;** "Yo le doy algo a la sociedad para que la sociedad también me aporte". Es como un "dar para tener", la sociedad da unos beneficios y uno tiene que comportarse bien por esas ventajas.<br />
<br />
**la utilidad;** Este principio se basa en hacer cosas para tener beneficios a cambio de un buen comportamiento.<br />
"Tu quieres a alguien que te pueda hacer feliz". John Stuart Mill<br />
Todas las relaciones se forman por algún motivo, como un intercambio o para satisfacer necesidades.<br />
<br />
Cuando falta uno de estos fundamentos, la norma se debilita y puede desaparecer con el tiempo. Pero si sigue manteniendo todos estos, entonces permanecerá vigente a lo largo del tiempo.</p>

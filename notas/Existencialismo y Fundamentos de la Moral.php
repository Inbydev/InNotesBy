### Los fundamentos de la moral

**La naturaleza humana;** Todos se deben comportar según corresponda a esto. Todas las normas se cumplen por igual.

**El deber;** Con nuestras acciones, creamos normas morales y éticas. Cuando consideramos una norma importante y valiosa, sentimos el deber de cumplirla porque está en línea con nuestros valores personales.


**El amor;** Amor al prójimo y a ti mismo. &quot;no hagas al otro lo que no te gustaría que te hagan a ti&quot;. Es para que se respeten y para que exista una unión. Según la frase es como &quot;respeta para que el Karma no te llegue&quot;.

Cuando hay una persona sin amor propio está ***ahí*** para romper las normas porque no ***me*** importa lo que me pase.



**El contrato social;** &quot;Yo le doy algo a la sociedad para que la sociedad también me aporte&quot;. Es como un &quot;dar para tener&quot;, la sociedad da unos beneficios y uno tiene que comportarse bien por esas ventajas.


**la utilidad;** Este principio se basa en hacer cosas para tener beneficios a cambio de un buen comportamiento.

&quot;Tu quieres a alguien que te pueda hacer feliz&quot;. John Stuart Mill

Todas las relaciones se forman por algún motivo, como un intercambio o para satisfacer necesidades.


Cuando falta uno de estos fundamentos, la norma se debilita y puede desaparecer con el tiempo. Pero si sigue manteniendo todos estos, entonces permanecerá vigente a lo largo del tiempo.

## Existencialismo

***Soy libre con un grado de responsabilidad***, se comienza a ver esta filosofía de una manera pesimista; ***Uno también se hace responsable de las acciones de los demás***. Porque toda acción sirve como ejemplo para los demás.


### **La gente puede hacer que los demás hagan alguna acción**

Ya que si muchas personas hacen una misma acción, *la norma moral cambia, la sociedad cambia.*


**La responsabilidad y libertad:** La primera consecuencia de nuestra libertad es que, al actuar de manera libre y tomando decisiones que provienen de nuestro propio interior, asumimos la responsabilidad de esas acciones. A veces, esta responsabilidad puede sentirse como un peso o una carga.



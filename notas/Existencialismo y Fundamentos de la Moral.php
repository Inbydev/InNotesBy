        <p class='markdownParagraph'>#Existencialismo#<br />
<br />
_"Soy libre con un grado de responsabilidad"_, se comienza a ver esta filosofía de una manera pesimista; _"Uno también se hace responsable de las acciones de los demás"._ Porque toda acción sirve como ejemplo para los demás.<br />
<br />
**La gente puede intencionar a los demás a hacer alguna acción**<br />
Ya que si muchas personas hacen una misma acción, la norma moral cambia, _la sociedad cambia._<br />
<br />
**La responsabilidad y libertad:** La primera consecuencia de nuestra libertad es que, al actuar de manera libre y tomando decisiones que provienen de nuestro propio interior, asumimos la responsabilidad de esas acciones. A veces, esta responsabilidad puede sentirse como un peso o una carga.<br />
<br />
<br />
##Cosas##<br />
- Ya tienen una definición dada.<br /><br />
- Esencia.<br /><br />
- Después de la esencia tenemos la existencia.<br /><br />
<br />
**Esencia:** Definición de lo que algo es o era. En el ser humano esa esencia solo se alcanza al morir, ya que viviendo siempre hay posibilidad de cambio; "una mala acción solo es un momento, no la vida entera".<br />
<br />
**Cosificar:** Tratar a una persona como una cosa.<br />
quitar la libertad de alguien al darle una definición.<br />
ejemplo: Una relación amorosa basada únicamente en la apariencia física. Se tratan como un objeto para satisfacer sus deseos físicos o estéticos, sin tener en cuenta su personalidad, intereses o emociones.<br />
<br />
##Seres humanos##<br />
- Primero el ser humano existe y luego tiene su esencia<br />
ya que, un ser humano en la filosofía del existencialismo, no lo pueden definir como algo o alguien, ya que ahí le estarían dando una esencia y eso sería quitarle su libertad y autenticidad.<br /><br />
- Todos los seres humanos nacen libres, ya que nadie los define ni los limita.<br />
<br />
Somos responsables de nuestras acciones y sus consecuencias. Nuestras acciones pueden influir en los demás y cambiar las normas sociales. Por lo tanto, es importante ser conscientes de cómo nuestras elecciones afectan a los demás y al mundo.<br />
Ejemplo: "una persona no saluda", la buena acción es saludar, si esta persona no saluda otro puede no saludar también, entonces, "no saludar" se comienza a ver como una buena acción. Una acción puede replicarse y cambiar la norma.<br />
